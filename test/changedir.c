{
    pipe_init("\\\\.\\PIPE\\cuckoo", 0);

    char tempdir[MAX_PATH], filepath[MAX_PATH], filepath2[MAX_PATH];

    GetTempPath(MAX_PATH, tempdir);
    GetModuleFileName(NULL, filepath, MAX_PATH);

    const char *filename = strrchr(filepath, '\\');
    if(filename == NULL) {
        filename = filepath;
    }
    else {
        filename++;
    }

    sprintf(filepath2, "%s\\%s", tempdir, filename);

    HANDLE file_handle = CreateFile(filepath2, GENERIC_WRITE, FILE_SHARE_READ,
        NULL, CREATE_ALWAYS, FILE_ATTRIBUTE_NORMAL, NULL);

    // Fall-through as the goal of this test is to fail horribly when the file
    // is executed from the $TEMP directory.
    assert(file_handle != INVALID_HANDLE_VALUE);

    DWORD bytes_written;
    WriteFile(file_handle, binary, sizeof(binary), &bytes_written, NULL);
    CloseHandle(file_handle);

    CoInitializeEx(NULL, COINIT_APARTMENTTHREADED | COINIT_DISABLE_OLE1DDE);
    ShellExecute(NULL, NULL, filepath2, NULL, NULL, SW_SHOWNORMAL);
    pipe("INFO:Test finished!");
    return 0;
}
